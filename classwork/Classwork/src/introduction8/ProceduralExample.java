package introduction8;

public class ProceduralExample {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		String[] names = {"Jillian","Jason","Jordan"};
		String[] years = {"Senior","Freshman","Sophomore"};
		System.out.println("Hi, my names is "+names[0]+
				" and I am a "+years[0]+".");
		System.out.println("Hi, my names is "+names[1]+
				" and I am a "+years[1]+".");
		System.out.println("Hi, my names is "+names[2]+
				" and I am a "+years[2]+".");
		
		
	}
	
}
